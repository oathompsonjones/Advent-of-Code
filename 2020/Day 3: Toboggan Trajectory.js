// Part 2
const slope = (right, down) => {
    let treeCount1 = 0;
    for (
        let x = 0, y = 0;
        x < input[0].length && y < input.length;
        x = (x + right) % input[0].length, y += down
    ) {
        if (input[y][x] === "#")
            treeCount1++;
    }

    return treeCount1;
};
